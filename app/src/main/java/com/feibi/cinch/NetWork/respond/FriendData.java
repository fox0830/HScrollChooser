package com.feibi.cinch.NetWork.respond;

public class FriendData {
    String lc_id;
    String lc_name;
    String lc_pic;
    String join_date;
    String on_status;

    public String getLc_id() {
        return lc_id;
    }

    public void setLc_id(String lc_id) {
        this.lc_id = lc_id;
    }

    public String getLc_name() {
        return lc_name;
    }

    public void setLc_name(String lc_name) {
        this.lc_name = lc_name;
    }

    public String getLc_pic() {
        return lc_pic;
    }

    public void setLc_pic(String lc_pic) {
        this.lc_pic = lc_pic;
    }

    public String getJoin_date() {
        return join_date;
    }

    public void setJoin_date(String join_date) {
        this.join_date = join_date;
    }

    public String getOn_status() {
        return on_status;
    }

    public void setOn_status(String on_status) {
        this.on_status = on_status;
    }
}
